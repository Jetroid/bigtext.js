HTMLDivElement.prototype.bigText = function(options){
	if (typeof options === 'undefined') { 
		options = {
			rotateText: null,
			fontSizeFactor: 0.8,
			maximumFontSize: null,
			limitingDimension: "both",
			horizontalAlign: "center",
			verticalAlign: "center",
			textAlign: "center",
			whiteSpace: "nowrap"
		};
	}
	
	
	
}
